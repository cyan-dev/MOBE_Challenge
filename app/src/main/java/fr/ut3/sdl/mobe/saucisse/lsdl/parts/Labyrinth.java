    public String transform(int coordX, int coordY, int width, int height) {
        if (struct[coordY][coordX] == 1) {
            // coordX or coordY are invalid
            throw new IllegalArgumentException();
        }

        String subLabyrinth = "";
        int minY = coordY - height/2;
        int maxY = coordY + height/2 + (height%2 == 1 ? 1 : 0);
        int minX = coordX - width/2;
        int maxX = coordX + width/2 + (width%2 == 1 ? 1 : 0);

        for (int y = minY; y < maxY; y++) {
            for (int x = minX; x < maxX; x++) {
                if (y < 0 || y > struct.length || x < 0 || x > struct[0].length || struct[y][x] == 1) {
                    subLabyrinth += "X";
                }else if (x == coordX && y == coordY) {
                    subLabyrinth += "O";
                } else {
                    subLabyrinth += " ";
                }
            }
            subLabyrinth += y != maxY-1 ? "\n" : "";
        }
        return subLabyrinth;
    }

    public int getCase(int x, int y) {
        return struct[y][x];
    }
